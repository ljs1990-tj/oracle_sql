-- CASE ~ WHEN
-- PROFESSOR
-- 1. PROFESSOR 테이블에서 400이상의 급여를 받으면 A, 
--		300~400 사이면 B, 나머지는 C로 출력
--    교수이름, 급여 등급(A~C)

SELECT 
    NAME,
    CASE 
        WHEN PAY >= 400 THEN 'A'
        WHEN PAY >= 300 THEN 'B'
        ELSE 'C'
    END AS PAY_GRADE
FROM PROFESSOR;






